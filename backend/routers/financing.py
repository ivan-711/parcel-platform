@router.get("/obligations", response_model=PaginatedObligations)
async def list_obligations(
    instrument_id: Optional[UUID] = Query(None),
    property_id: Optional[UUID] = Query(None),
    obligation_status: Optional[str] = Query(None, alias="status"),
    severity: Optional[str] = Query(None),
    due_before: Optional[date] = Query(None),
    due_after: Optional[date] = Query(None),
    page: int = Query(1, ge=1),
    per_page: int = Query(20, ge=1, le=100),
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    # Resurface snoozed obligations whose snooze date has passed
    db.query(Obligation).filter(
        Obligation.created_by == current_user.id,
        Obligation.deleted_at.is_(None),
        Obligation.status == "snoozed",
        Obligation.next_due <= date.today(),
    ).update({"status": "active"}, synchronize_session="fetch")

    q = db.query(Obligation).filter(
        Obligation.created_by == current_user.id,
        Obligation.deleted_at.is_(None),
    )
    if instrument_id:
        q = q.filter(Obligation.instrument_id == instrument_id)
    if property_id:
        q = q.filter(Obligation.property_id == property_id)
    if obligation_status:
        q = q.filter(Obligation.status == obligation_status)
    if severity:
        q = q.filter(Obligation.severity == severity)
    if due_before:
        q = q.filter(Obligation.next_due <= due_before)
    if due_after:
        q = q.filter(Obligation.next_due >= due_after)

    total = q.count()

    obligations = (
        q.order_by(Obligation.next_due.asc().nullslast())
        .offset((page - 1) * per_page)
        .limit(per_page)
        .all()
    )

    items = []
    for obl in obligations:
        inst = db.query(FinancingInstrument).get(obl.instrument_id)
        prop = db.query(Property).get(obl.property_id)
        inst_name = inst.name if inst else ""
        prop_addr = prop.address_line1 if prop else ""
        computed = _build_obligation_computed(obl, inst_name, prop_addr)
        item = ObligationWithComputed.model_validate(obl, from_attributes=True)
        for k, v in computed.items():
            setattr(item, k, v)
        items.append(item)

    return PaginatedObligations(
        items=items,
        total=total,
        page=page,
        per_page=per_page,
        pages=math.ceil(total / per_page) if total else 0,
    )


@router.get("/obligations/upcoming", response_model=ObligationGrouped)
async def upcoming_obligations(
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    # Resurface snoozed obligations whose snooze date has passed
    db.query(Obligation).filter(
        Obligation.created_by == current_user.id,
        Obligation.deleted_at.is_(None),
        Obligation.status == "snoozed",
        Obligation.next_due <= date.today(),
    ).update({"status": "active"}, synchronize_session="fetch")

    cutoff = date.today() + timedelta(days=90)
    obligations = (
        db.query(Obligation)
        .filter(
            Obligation.created_by == current_user.id,
            Obligation.deleted_at.is_(None),
            Obligation.status == "active",
            Obligation.next_due <= cutoff,
            Obligation.next_due.isnot(None),
        )
        .order_by(Obligation.next_due.asc())
        .all()
    )

    grouped = ObligationGrouped()
    for obl in obligations:
        inst = db.query(FinancingInstrument).get(obl.instrument_id)
        prop = db.query(Property).get(obl.property_id)
        inst_name = inst.name if inst else ""
        prop_addr = prop.address_line1 if prop else ""
        computed = _build_obligation_computed(obl, inst_name, prop_addr)
        item = ObligationWithComputed.model_validate(obl, from_attributes=True)
        for k, v in computed.items():
            setattr(item, k, v)

        if obl.severity == "critical":
            grouped.critical.append(item)
        elif obl.severity == "high":
            grouped.high.append(item)
        else:
            grouped.normal.append(item)

    try:
        track_event(current_user.id, "obligation_briefing_generated", {
            "count": len(obligations),
            "critical_count": len(grouped.critical),
        })
    except Exception:
        pass

    return grouped


