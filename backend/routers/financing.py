@router.get("/payments", response_model=PaginatedPayments)
async def list_payments(
    instrument_id: Optional[UUID] = Query(None),
    property_id: Optional[UUID] = Query(None),
    direction: Optional[str] = Query(None),
    date_from: Optional[date] = Query(None),
    date_to: Optional[date] = Query(None),
    page: int = Query(1, ge=1),
    per_page: int = Query(20, ge=1, le=100),
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    q = db.query(Payment).filter(
        Payment.created_by == current_user.id,
        Payment.deleted_at.is_(None),
    )
    if instrument_id:
        q = q.filter(Payment.instrument_id == instrument_id)
    if property_id:
        q = q.filter(Payment.property_id == property_id)
    if direction:
        q = q.filter(Payment.direction == direction)
    if date_from:
        q = q.filter(Payment.payment_date >= date_from)
    if date_to:
        q = q.filter(Payment.payment_date <= date_to)

    total = q.count()

    payments = (
        q.order_by(Payment.payment_date.desc())
        .offset((page - 1) * per_page)
        .limit(per_page)
        .all()
    )

    return PaginatedPayments(
        items=[PaymentResponse.model_validate(p, from_attributes=True) for p in payments],
        total=total,
        page=page,
        per_page=per_page,
        pages=math.ceil(total / per_page) if total else 0,
    )


@router.post(
    "/payments",
    response_model=PaymentResponse,
    status_code=status.HTTP_201_CREATED,
)
async def create_payment(
    body: CreatePaymentRequest,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    # Validate instrument ownership
    inst = _get_instrument_or_404(db, body.instrument_id, current_user.id)

    # Validate obligation belongs to instrument and user
    if body.obligation_id:
        obl_check = db.query(Obligation).filter(
            Obligation.id == body.obligation_id,
            Obligation.instrument_id == body.instrument_id,
            Obligation.created_by == current_user.id,
            Obligation.deleted_at.is_(None),
        ).first()
        if not obl_check:
            raise HTTPException(status_code=400, detail={
                "error": "Obligation not found or does not belong to this instrument",
                "code": "OBLIGATION_MISMATCH",
            })

    # Estimate P&I if not provided
    principal = body.principal_portion
    interest = body.interest_portion
    if principal is None and body.amount:
        est_p, est_i = _estimate_pi_split(inst, float(body.amount))
        principal = est_p
        interest = est_i

    _update_balance_from_payment(
        inst, float(body.amount),
        float(principal) if principal is not None else None,
        float(interest) if interest is not None else None,
    )

    payment_data = body.model_dump()
    payment_data['principal_portion'] = principal
    payment_data['interest_portion'] = interest
    payment_data['property_id'] = inst.property_id  # S1: derive from instrument
    payment = Payment(
        **payment_data,
        created_by=current_user.id,
        team_id=current_user.team_id,
    )
    db.add(payment)

    # Advance obligation if linked
    if body.obligation_id:
        obl = (
            db.query(Obligation)
            .filter(
                Obligation.id == body.obligation_id,
                Obligation.created_by == current_user.id,
                Obligation.deleted_at.is_(None),
            )
            .first()
        )
        if obl and obl.recurrence and obl.recurrence != "one_time" and obl.next_due:
            months = {"monthly": 1, "quarterly": 3, "annually": 12}.get(obl.recurrence, 1)
            obl.next_due = _add_months(obl.next_due, months)

    db.commit()
    db.refresh(payment)

    try:
        track_event(current_user.id, "payment_recorded", {
            "direction": body.direction,
            "amount": float(body.amount),
            "instrument_type": inst.instrument_type,
        })
    except Exception:
        pass

    return PaymentResponse.model_validate(payment, from_attributes=True)


@router.get("/payments/summary", response_model=PaymentSummaryResponse)
async def payment_summary(
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    twelve_months_ago = date.today() - timedelta(days=365)

    payments = (
        db.query(Payment)
        .filter(
            Payment.created_by == current_user.id,
            Payment.deleted_at.is_(None),
            Payment.payment_date >= twelve_months_ago,
        )
        .all()
    )

    total_outgoing = 0.0
    total_incoming = 0.0
    outgoing_by_prop: dict[UUID, float] = {}
    incoming_by_prop: dict[UUID, float] = {}
    monthly_buckets: dict[str, dict] = {}

    for p in payments:
        amt = float(p.amount or 0)
        month_key = p.payment_date.strftime("%Y-%m")

        if month_key not in monthly_buckets:
            monthly_buckets[month_key] = {"incoming": 0.0, "outgoing": 0.0}

        if p.direction == "outgoing":
            total_outgoing += amt
            outgoing_by_prop[p.property_id] = outgoing_by_prop.get(p.property_id, 0) + amt
            monthly_buckets[month_key]["outgoing"] += amt
        else:
            total_incoming += amt
            incoming_by_prop[p.property_id] = incoming_by_prop.get(p.property_id, 0) + amt
            monthly_buckets[month_key]["incoming"] += amt

    # Resolve property addresses
    prop_ids = set(outgoing_by_prop.keys()) | set(incoming_by_prop.keys())
    props = {}
    if prop_ids:
        for prop in db.query(Property).filter(Property.id.in_(prop_ids)).all():
            props[prop.id] = prop.address_line1 or str(prop.id)

    months_count = max(len(monthly_buckets), 1)

    outgoing_list = [
        PropertyPaymentSummary(
            property_id=pid,
            address=props.get(pid, str(pid)),
            total=round(total, 2),
        )
        for pid, total in outgoing_by_prop.items()
    ]
    incoming_list = [
        PropertyPaymentSummary(
            property_id=pid,
            address=props.get(pid, str(pid)),
            total=round(total, 2),
        )
        for pid, total in incoming_by_prop.items()
    ]

    history = sorted([
        MonthlyHistory(
            month=k,
            incoming=round(v["incoming"], 2),
            outgoing=round(v["outgoing"], 2),
            net=round(v["incoming"] - v["outgoing"], 2),
        )
        for k, v in monthly_buckets.items()
    ], key=lambda h: h.month)

    return PaymentSummaryResponse(
        total_outgoing_monthly=round(total_outgoing / months_count, 2),
        total_incoming_monthly=round(total_incoming / months_count, 2),
        net_monthly=round((total_incoming - total_outgoing) / months_count, 2),
        outgoing_by_property=outgoing_list,
        incoming_by_property=incoming_list,
        payment_history=history,
    )


