@router.get("/dashboard", response_model=FinancingDashboardResponse)
async def financing_dashboard(
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    instruments = (
        db.query(FinancingInstrument)
        .filter(
            FinancingInstrument.created_by == current_user.id,
            FinancingInstrument.deleted_at.is_(None),
            FinancingInstrument.status == "active",
        )
        .all()
    )

    total_balance = sum(float(i.current_balance or 0) for i in instruments)
    total_monthly_obligations = sum(
        float(i.monthly_payment or 0) for i in instruments if not i.is_wrap
    )
    total_monthly_income = sum(
        float(i.wrap_payment or 0) for i in instruments if i.is_wrap
    )

    # Upcoming balloons (next 3)
    balloon_obls = (
        db.query(Obligation)
        .filter(
            Obligation.created_by == current_user.id,
            Obligation.deleted_at.is_(None),
            Obligation.obligation_type == "balloon_payment",
            Obligation.status == "active",
            Obligation.next_due.isnot(None),
        )
        .order_by(Obligation.next_due.asc())
        .limit(3)
        .all()
    )
    upcoming_balloons = []
    for bobl in balloon_obls:
        inst = db.query(FinancingInstrument).get(bobl.instrument_id)
        prop = db.query(Property).get(bobl.property_id)
        upcoming_balloons.append(BalloonAlert(
            instrument_name=inst.name if inst else "",
            property_address=prop.address_line1 if prop else "",
            balloon_date=bobl.next_due,
            balloon_amount=bobl.amount,
            days_until=(bobl.next_due - date.today()).days if bobl.next_due else None,
        ))

    # Wrap spreads
    wrap_spreads = []
    for inst in instruments:
        if inst.is_wrap and inst.underlying_instrument_id:
            underlying = (
                db.query(FinancingInstrument)
                .filter(
                    FinancingInstrument.id == inst.underlying_instrument_id,
                    FinancingInstrument.created_by == current_user.id,
                    FinancingInstrument.deleted_at.is_(None),
                )
                .first()
            )
            if underlying:
                spread = calculate_wrap_spread(inst, underlying)
                prop = db.query(Property).get(inst.property_id)
                wrap_spreads.append(WrapSpreadItem(
                    property_address=prop.address_line1 if prop else "",
                    monthly_spread=spread["monthly_spread"],
                    annual_spread=spread["annual_spread"],
                ))

    # Due-on-sale risks
    due_on_sale_risks = []
    for inst in instruments:
        if inst.is_sub_to and inst.due_on_sale_risk:
            prop = db.query(Property).get(inst.property_id)
            due_on_sale_risks.append(DueOnSaleRiskItem(
                property_address=prop.address_line1 if prop else "",
                instrument_name=inst.name,
                risk_level=inst.due_on_sale_risk,
            ))

    try:
        track_event(current_user.id, "financing_dashboard_viewed", {
            "total_instruments": len(instruments),
            "has_wraps": any(i.is_wrap for i in instruments),
            "has_sub_to": any(i.is_sub_to for i in instruments),
        })
    except Exception:
        pass

    return FinancingDashboardResponse(
        total_instruments=len(instruments),
        total_balance=round(total_balance, 2),
        total_monthly_obligations=round(total_monthly_obligations, 2),
        total_monthly_income=round(total_monthly_income, 2),
        net_monthly_cash_flow=round(total_monthly_income - total_monthly_obligations, 2),
        upcoming_balloons=upcoming_balloons,
        wrap_spreads=wrap_spreads,
        due_on_sale_risks=due_on_sale_risks,
    )
