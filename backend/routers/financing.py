@router.get("/instruments", response_model=PaginatedInstruments)
async def list_instruments(
    property_id: Optional[UUID] = Query(None),
    instrument_status: Optional[str] = Query(None, alias="status"),
    instrument_type: Optional[str] = Query(None),
    page: int = Query(1, ge=1),
    per_page: int = Query(20, ge=1, le=100),
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    q = db.query(FinancingInstrument).filter(
        FinancingInstrument.created_by == current_user.id,
        FinancingInstrument.deleted_at.is_(None),
    )
    if property_id:
        q = q.filter(FinancingInstrument.property_id == property_id)
    if instrument_status:
        q = q.filter(FinancingInstrument.status == instrument_status)
    if instrument_type:
        q = q.filter(FinancingInstrument.instrument_type == instrument_type)

    total = q.count()

    instruments = (
        q.join(Property, Property.id == FinancingInstrument.property_id)
        .order_by(Property.address_line1, FinancingInstrument.position)
        .offset((page - 1) * per_page)
        .limit(per_page)
        .all()
    )

    items = []
    for inst in instruments:
        extras = _compute_instrument_extras(inst, db)
        item = InstrumentListItem.model_validate(inst, from_attributes=True)
        for k, v in extras.items():
            setattr(item, k, v)
        items.append(item)

    return PaginatedInstruments(
        items=items,
        total=total,
        page=page,
        per_page=per_page,
        pages=math.ceil(total / per_page) if total else 0,
    )


@router.get("/instruments/{instrument_id}", response_model=InstrumentDetailResponse)
async def get_instrument(
    instrument_id: UUID,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    inst = _get_instrument_or_404(db, instrument_id, current_user.id)

    # Active obligations
    obligations = (
        db.query(Obligation)
        .filter(
            Obligation.instrument_id == inst.id,
            Obligation.deleted_at.is_(None),
        )
        .order_by(Obligation.next_due.asc().nullslast())
        .all()
    )

    # Last 5 payments
    recent_payments = (
        db.query(Payment)
        .filter(
            Payment.instrument_id == inst.id,
            Payment.deleted_at.is_(None),
        )
        .order_by(Payment.payment_date.desc())
        .limit(5)
        .all()
    )

    # Wrap spread
    wrap_spread = None
    if inst.is_wrap and inst.underlying_instrument_id:
        underlying = (
            db.query(FinancingInstrument)
            .filter(
                FinancingInstrument.id == inst.underlying_instrument_id,
                FinancingInstrument.created_by == current_user.id,
                FinancingInstrument.deleted_at.is_(None),
            )
            .first()
        )
        if underlying:
            spread = calculate_wrap_spread(inst, underlying)
            wrap_spread = WrapSpreadSummary(**spread)

    # Amortization schedule
    amortization = []
    if inst.current_balance and inst.interest_rate and inst.monthly_payment:
        amortization = calculate_amortization_schedule(
            balance=float(inst.current_balance),
            annual_rate=float(inst.interest_rate),
            monthly_payment=float(inst.monthly_payment),
            months=12,
            start_date=date.today(),
        )

    resp = InstrumentDetailResponse.model_validate(inst, from_attributes=True)
    resp.obligations = [ObligationResponse.model_validate(o, from_attributes=True) for o in obligations]
    resp.recent_payments = [PaymentResponse.model_validate(p, from_attributes=True) for p in recent_payments]
    resp.wrap_spread = wrap_spread
    resp.amortization_schedule = [AmortizationEntry(**e) for e in amortization]
    return resp


@router.post(
    "/instruments",
    response_model=FinancingInstrumentResponse,
    status_code=status.HTTP_201_CREATED,
)
async def create_instrument(
    body: CreateFinancingInstrumentRequest,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    _validate_property_ownership(db, body.property_id, current_user.id)

    # Validate wrap underlying
    if body.instrument_type == "wrap_mortgage" and body.underlying_instrument_id:
        underlying = _get_instrument_or_404(db, body.underlying_instrument_id, current_user.id)
        if underlying.property_id != body.property_id:
            raise HTTPException(
                status_code=400,
                detail={
                    "error": "Underlying instrument must belong to the same property",
                    "code": "INSTRUMENT_PROPERTY_MISMATCH",
                },
            )

    # Auto-set due_on_sale_risk for sub-to
    data = body.model_dump()
    if body.instrument_type == "sub_to_mortgage" and not body.due_on_sale_risk:
        risk_map = {"conventional": "high", "fha": "low", "va": "low", "portfolio": "medium"}
        loan_type = (data.get("rate_type") or "").lower()
        data["due_on_sale_risk"] = risk_map.get(loan_type, "high")

    instrument = FinancingInstrument(
        **data,
        created_by=current_user.id,
        team_id=current_user.team_id,
    )
    db.add(instrument)
    db.flush()

    # Generate obligations
    obligations = generate_obligations(instrument)
    for obl in obligations:
        obl.created_by = current_user.id
        obl.team_id = current_user.team_id
    db.add_all(obligations)
    db.commit()
    db.refresh(instrument)

    try:
        track_event(current_user.id, "instrument_created", {
            "instrument_type": instrument.instrument_type,
            "has_balloon": instrument.has_balloon,
            "is_wrap": instrument.is_wrap,
            "is_sub_to": instrument.is_sub_to,
        })
    except Exception:
        pass

    return FinancingInstrumentResponse.model_validate(instrument, from_attributes=True)


@router.patch("/instruments/{instrument_id}", response_model=FinancingInstrumentResponse)
async def update_instrument(
    instrument_id: UUID,
    body: UpdateFinancingInstrumentRequest,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    inst = _get_instrument_or_404(db, instrument_id, current_user.id)
    update_data = body.model_dump(exclude_unset=True)

    # Validate underlying_instrument_id on update
    new_underlying = update_data.get("underlying_instrument_id")
    if new_underlying is not None:
        # No self-reference
        if str(new_underlying) == str(instrument_id):
            raise HTTPException(status_code=400, detail={
                "error": "Instrument cannot reference itself",
                "code": "SELF_REFERENCE",
            })
        # Must belong to same user and same property
        underlying = _get_instrument_or_404(db, new_underlying, current_user.id)
        if underlying.property_id != inst.property_id:
            raise HTTPException(status_code=400, detail={
                "error": "Underlying instrument must belong to the same property",
                "code": "INSTRUMENT_PROPERTY_MISMATCH",
            })
        # Check for circular reference
        seen = {str(instrument_id)}
        current = underlying
        while current.underlying_instrument_id:
            uid = str(current.underlying_instrument_id)
            if uid in seen:
                raise HTTPException(status_code=400, detail={
                    "error": "Circular instrument reference detected",
                    "code": "CIRCULAR_REFERENCE",
                })
            seen.add(uid)
            current = db.query(FinancingInstrument).filter(
                FinancingInstrument.id == current.underlying_instrument_id,
                FinancingInstrument.deleted_at.is_(None),
            ).first()
            if not current:
                break

    for key, value in update_data.items():
        setattr(inst, key, value)

    # Regenerate obligations if terms changed
    if update_data.keys() & _TERM_FIELDS:
        now = datetime.utcnow()
        db.query(Obligation).filter(
            Obligation.instrument_id == inst.id,
            Obligation.deleted_at.is_(None),
            Obligation.obligation_type.in_(_AUTO_OBLIGATION_TYPES),
        ).update({"deleted_at": now}, synchronize_session="fetch")

        new_obligations = generate_obligations(inst)
        for obl in new_obligations:
            obl.created_by = current_user.id
            obl.team_id = current_user.team_id
        db.add_all(new_obligations)

    db.commit()
    db.refresh(inst)

    try:
        track_event(current_user.id, "instrument_updated", {
            "instrument_id": str(instrument_id),
            "fields_changed": list(update_data.keys()),
        })
    except Exception:
        pass

    return FinancingInstrumentResponse.model_validate(inst, from_attributes=True)


