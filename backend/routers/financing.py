@router.post("/obligations/{obligation_id}/complete", response_model=ObligationResponse)
async def complete_obligation(
    obligation_id: UUID,
    body: ObligationCompleteRequest,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    obl = _get_obligation_or_404(db, obligation_id, current_user.id)

    was_overdue = False
    days_delta = 0
    if obl.next_due:
        days_delta = (date.today() - obl.next_due).days
        was_overdue = days_delta > 0

    # Create payment if details provided
    if body.payment_amount is not None:
        inst = db.query(FinancingInstrument).get(obl.instrument_id)
        principal, interest = _update_balance_from_payment(
            inst, float(body.payment_amount), None, None
        ) if inst else (0.0, 0.0)
        payment = Payment(
            instrument_id=obl.instrument_id,
            obligation_id=obl.id,
            property_id=obl.property_id,
            created_by=current_user.id,
            team_id=current_user.team_id,
            payment_type="regular",
            amount=body.payment_amount,
            principal_portion=principal,
            interest_portion=interest,
            payment_date=body.payment_date or date.today(),
            payment_method=body.payment_method,
            direction="outgoing",
        )
        db.add(payment)

    # Advance recurring obligations or complete one-time
    if obl.recurrence and obl.recurrence != "one_time" and obl.next_due:
        months = {"monthly": 1, "quarterly": 3, "annually": 12}.get(obl.recurrence, 1)
        obl.next_due = _add_months(obl.next_due, months)
        obl.status = "active"
    else:
        obl.status = "completed"

    db.commit()
    db.refresh(obl)

    try:
        track_event(current_user.id, "obligation_completed", {
            "obligation_type": obl.obligation_type,
            "was_overdue": was_overdue,
            "days_early_or_late": days_delta,
        })
    except Exception:
        pass

    return ObligationResponse.model_validate(obl, from_attributes=True)


