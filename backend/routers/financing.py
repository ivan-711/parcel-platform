def _compute_instrument_extras(
    instrument: FinancingInstrument, db: Session,
) -> dict:
    """Compute derived fields for an instrument."""
    extras: dict = {
        "months_remaining": None,
        "next_payment_due": None,
        "payoff_amount_estimate": None,
        "wrap_spread": None,
    }

    # Months remaining
    if instrument.maturity_date:
        delta = (instrument.maturity_date - date.today()).days
        extras["months_remaining"] = max(delta // 30, 0)

    # Next payment due — from first active monthly obligation
    monthly_obl = (
        db.query(Obligation)
        .filter(
            Obligation.instrument_id == instrument.id,
            Obligation.obligation_type == "monthly_payment",
            Obligation.status == "active",
            Obligation.deleted_at.is_(None),
        )
        .first()
    )
    if monthly_obl and monthly_obl.next_due:
        extras["next_payment_due"] = monthly_obl.next_due

    # Payoff estimate
    if instrument.current_balance is not None:
        extras["payoff_amount_estimate"] = float(instrument.current_balance)

    # Wrap spread
    if instrument.is_wrap and instrument.underlying_instrument_id:
        underlying = (
            db.query(FinancingInstrument)
            .filter(
                FinancingInstrument.id == instrument.underlying_instrument_id,
                FinancingInstrument.created_by == instrument.created_by,
                FinancingInstrument.deleted_at.is_(None),
            )
            .first()
        )
        if underlying:
            spread = calculate_wrap_spread(instrument, underlying)
            extras["wrap_spread"] = WrapSpreadSummary(**spread)

    return extras


def _build_obligation_computed(
    obl: Obligation, instrument_name: str = "", property_address: str = "",
) -> dict:
    """Build computed fields for an obligation."""
    days = None
    is_overdue = False
    if obl.next_due:
        days = (obl.next_due - date.today()).days
        if days < 0 and obl.status == "active":
            is_overdue = True
    return {
        "days_until_due": days,
        "is_overdue": is_overdue,
        "instrument_name": instrument_name,
        "property_address": property_address,
    }


def _estimate_pi_split(instrument: FinancingInstrument, amount: float) -> tuple[float, float]:
    """Estimate principal and interest portions from instrument terms.
    Returns (principal, interest). If rate is 0 or null, all principal."""
    rate = float(instrument.interest_rate or 0)
    balance = float(instrument.current_balance or 0)
    if rate <= 0 or balance <= 0:
        return (amount, 0.0)
    monthly_rate = rate / 100 / 12
    interest = balance * monthly_rate
    principal = amount - interest
    if principal < 0:
        principal = 0.0
        interest = amount
    return (round(principal, 2), round(interest, 2))


def _update_balance_from_payment(
    instrument: FinancingInstrument,
    amount: float,
    principal_portion: float | None,
    interest_portion: float | None,
) -> tuple[float, float]:
    """Update instrument balance and return (principal, interest) used.

    If principal_portion is provided, use it directly.
    Otherwise estimate from instrument terms.
    Balance is clamped to 0 (never goes negative).
    If balance reaches 0, instrument is marked paid_off.
    """
    if instrument.current_balance is None:
        return (0.0, 0.0)

    if principal_portion is not None:
        principal = float(principal_portion)
        interest = float(interest_portion or 0)
    else:
        principal, interest = _estimate_pi_split(instrument, amount)

    new_balance = max(float(instrument.current_balance) - principal, 0)
    instrument.current_balance = new_balance

    # Auto mark paid off
    if new_balance == 0 and instrument.status == "active":
        instrument.status = "paid_off"

    return (principal, interest)


