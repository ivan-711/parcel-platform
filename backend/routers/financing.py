def _get_instrument_or_404(
    db: Session, instrument_id: UUID, user_id: UUID,
) -> FinancingInstrument:
    inst = (
        db.query(FinancingInstrument)
        .filter(
            FinancingInstrument.id == instrument_id,
            FinancingInstrument.created_by == user_id,
            FinancingInstrument.deleted_at.is_(None),
        )
        .first()
    )
    if not inst:
        raise HTTPException(
            status_code=404,
            detail={"error": "Instrument not found", "code": "INSTRUMENT_NOT_FOUND"},
        )
    return inst


